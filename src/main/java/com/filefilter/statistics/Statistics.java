package com.filefilter.statistics;

/**
 * Контракт для сбора и вывода статистики по данным.
 * <p>
 * Реализации должны обеспечивать:
 * <ul>
 *     <li>{@link #addValue(String)} — добавить новое значение для учёта в статистике.</li>
 *     <li>{@link #printShort()} — вывести краткую статистику (обычно только количество элементов).</li>
 *     <li>{@link #printFull()} — вывести расширенную статистику (дополнительные параметры: min, max, сумма, среднее и пр. — зависит от реализации).</li>
 * </ul>
 */
public interface Statistics {
    /**
     * Добавляет значение для учёта в статистике.
     * @param value строковое представление значения (парсится в реализации)
     */
    void addValue(String value);

    /**
     * Выводит краткую статистику по накопленным данным.
     */
    void printShort();

    /**
     * Выводит расширенную статистику по накопленным данным.
     */
    void printFull();
}
